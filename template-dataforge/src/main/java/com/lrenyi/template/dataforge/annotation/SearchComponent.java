package com.lrenyi.template.dataforge.annotation;

/**
 * 搜索组件类型
 */
public enum SearchComponent {
    
    /** 输入框 */
    INPUT,
    
    /** 下拉选择框 */
    SELECT,
    
    /** 多选下拉框 */
    MULTI_SELECT,
    
    /** 日期选择器 */
    DATE,
    
    /** 日期时间选择器 */
    DATETIME,
    
    /** 日期范围选择器 */
    DATE_RANGE,
    
    /** 数字范围选择器 */
    NUMBER_RANGE,
    
    /** 开关 */
    SWITCH,
    
    /** 树形选择器 */
    TREE_SELECT,
    
    /** 级联选择器 */
    CASCADER
}